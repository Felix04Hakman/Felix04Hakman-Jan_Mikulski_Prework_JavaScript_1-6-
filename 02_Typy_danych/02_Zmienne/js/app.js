
let numberValue = 5; // Ta zmienna jest typu number i przechowuje wartość 5.
console.log(numberValue);
console.log(typeof numberValue);

let stringValue = "napis"; // Ta zmienna jest typu string i przechowuje wartość "napis".
console.log(stringValue);
console.log(typeof stringValue);

let mixedValue = 5 + "nowy";// Ta zmienne są typu number i string przechowują wartość 5 i "nowy".
console.log(mixedValue);
console.log(typeof mixedValue);

let logicValue = true;// Ta zmienna jest typu logicznego i przechowuje wartość true.
console.log(logicValue);
console.log(typeof logicValue);

let nullValue = null; // Ta zmienne są typu specjalnego i przechowują wartość null.
console.log(nullValue);
console.log(typeof nullValue);

